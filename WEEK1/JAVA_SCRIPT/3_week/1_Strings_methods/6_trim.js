/**

TRIM : 
 ->It identifies and removes any whitespace characters from the beginning and end of the string.

 <str_name>.trim() 
 -> no parameters 
  
 */


 var str1 = "   Hello,  world!  ";
 var str2 = "ri   shi" 


 console.log(str1.trim()); //Hello,  world!

 console.log(str2.trim()); //ri   shi